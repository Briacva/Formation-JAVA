// Write a JavaScript program to check two given numbers and return true if one of the number is 50 or if their sum is 50

function checkIntegers(int1, int2) {
    if (int1 === 50 || int2 === 20 || int1 + int2 === 50) {
        return true;
    }
    else {
        return false;
    }
}

